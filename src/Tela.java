    public void verificar(JButton[] btns, String[] times){
        if(times[0].equals(times[1])){
            JOptionPane.showMessageDialog(null, "Você acertou!");   
            btns[0].setEnabled(false);
           btns[1].setEnabled(false);
           this.acertos++;
        } else{
            JOptionPane.showMessageDialog(null, "Você errou", "Erro", JOptionPane.ERROR_MESSAGE);
            btns[0].setText("?");
            btns[1].setText("?");
        }
        if(acertos == 6){
            JOptionPane.showMessageDialog(null, "Parabéns, você finalizou o Jogo");
            System.exit(0);
        }
        this.click = 0;
        //quandio for adidcionar, o mesmo zera
    }
